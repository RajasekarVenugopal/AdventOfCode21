findIntersectingPoints = (points) => {
  const visitedPoints = {};
  let intersectingPoints = 0;
  points.forEach((point) => {
    //   Part 1
    if (point.from.x === point.to.x) {
      for (
        let i = Math.min(point.from.y, point.to.y);
        i <= Math.max(point.from.y, point.to.y);
        i++
      ) {
        const key = `${point.from.x}-${i}`;
        visitedPoints[key] = visitedPoints[key] ? visitedPoints[key] + 1 : 1;
      }
    } else if (point.from.y === point.to.y) {
      for (
        let i = Math.min(point.from.x, point.to.x);
        i <= Math.max(point.from.x, point.to.x);
        i++
      ) {
        const key = `${i}-${point.from.y}`;
        visitedPoints[key] = visitedPoints[key] ? visitedPoints[key] + 1 : 1;
      }
    } else {
      // Part 2
      let xDir = point.to.x - point.from.x > 0 ? 1 : -1;
      let yDir = point.to.y - point.from.y > 0 ? 1 : -1;
      for (let i = 0; i <= Math.abs(point.to.x - point.from.x); i++) {
        const key = `${point.from.x + xDir * i}-${point.from.y + yDir * i}`;
        visitedPoints[key] = visitedPoints[key] ? visitedPoints[key] + 1 : 1;
      }
    }
  });
  Object.keys(visitedPoints).forEach((key) => {
    if (visitedPoints[key] > 1) {
      intersectingPoints++;
    }
  });
  return intersectingPoints;
};

